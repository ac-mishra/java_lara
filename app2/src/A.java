class A 
{
	public static void main(String[] args) 
	{
		System.out.println("Hello World!"); //String literal
		System.out.println(231);			//int literal
		System.out.println(3.56);			//double literal
		System.out.println('e');			//char literal ( only single letter & integer value 0-9)
		System.out.println(true);			//boolean literal(lower case only)
	}
}
